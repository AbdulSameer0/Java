package MethodOverloading;

public class UseOfMathodOverLoading {
			
				   static void display(int a) {
					   
				System.out.println(a);
				
					
				}
			       void display(int a, int b) {
			    	   System.out.println(a+b);
			       }
			       public static void main(String[] args) {
					display(1);
					display(8);
					
				}
			}


